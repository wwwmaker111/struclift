# -*- coding: utf-8 -*-
"""Export source functions and current Stage2 A/B predicted skeletons.

This is for manual control-flow comparison.  It deliberately does not trust
old skeleton text stored in a v9/v10 JSONL.  The JSONL is used as the sample
pool and source/binary feature carrier; the AB skeleton is regenerated by
loading a Stage2 checkpoint, running current Module A/B, and serializing the
emitted skeleton with the current rule builder.
"""

